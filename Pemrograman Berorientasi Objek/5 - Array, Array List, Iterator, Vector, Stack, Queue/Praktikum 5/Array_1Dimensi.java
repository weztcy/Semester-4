public class Array_1Dimensi {
    public static void main(String[] args) {
        int Array[] = {1, 2, 3, 4, 5};
        System.out.println("Panjang Array = " + Array.length);
        System.out.print("Data Array = ");
        for (int i = 0; i < Array.length; i++) {
            System.out.print(Array[i] + " ");
        }
    }
}
